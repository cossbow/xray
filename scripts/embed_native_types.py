#!/usr/bin/env python3
"""Embed stdlib/types/*.xr files into a C header as string literals.

Usage: python3 scripts/embed_native_types.py stdlib/types/ > src/frontend/analyzer/xnative_type_defs.inc

The generated file provides:
  - XR_NATIVE_TYPE_DEFS: X-macro list of (name, source_string)
  - Individual static const char *xr_native_def_XXX strings
"""

import os
import sys
import glob

def escape_c_string(s):
    """Escape a string for embedding as a C string literal."""
    result = []
    for ch in s:
        if ch == '\\':
            result.append('\\\\')
        elif ch == '"':
            result.append('\\"')
        elif ch == '\n':
            result.append('\\n')
        elif ch == '\t':
            result.append('\\t')
        else:
            result.append(ch)
    return ''.join(result)

def main():
    if len(sys.argv) < 2:
        print("Usage: embed_native_types.py <types_dir>", file=sys.stderr)
        sys.exit(1)

    types_dir = sys.argv[1]
    xr_files = sorted(glob.glob(os.path.join(types_dir, "*.xr")))

    if not xr_files:
        print(f"No .xr files found in {types_dir}", file=sys.stderr)
        sys.exit(1)

    print("/* Auto-generated by scripts/embed_native_types.py — DO NOT EDIT */")
    print()

    entries = []
    for path in xr_files:
        name = os.path.splitext(os.path.basename(path))[0]
        with open(path, 'r') as f:
            content = f.read()
        var_name = f"xr_native_def_{name}"
        escaped = escape_c_string(content)
        print(f'static const char {var_name}[] = "{escaped}";')
        print()
        entries.append((name, var_name))

    # Generate X-macro list
    print("#define XR_NATIVE_TYPE_DEFS(X) \\")
    for i, (name, var_name) in enumerate(entries):
        suffix = " \\" if i < len(entries) - 1 else ""
        print(f'    X("{name}", {var_name}){suffix}')
    print()

if __name__ == "__main__":
    main()
